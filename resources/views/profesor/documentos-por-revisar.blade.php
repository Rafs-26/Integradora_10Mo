<script>
    function aprobarDocumento(documentoId) {
        const modal = document.getElementById('modal-aprobar');
        const form = document.getElementById('form-aprobar');
        form.action = `/teacher/documentos/${documentoId}/aprobar`;
        modal.classList.remove('hidden');
    }
    
    function rechazarDocumento(documentoId) {
        const modal = document.getElementById('modal-rechazar');
        const form = document.getElementById('form-rechazar');
        form.action = `/teacher/documentos/${documentoId}/rechazar`;
        modal.classList.remove('hidden');
    }
    
    function cerrarModal(modalId) {
        document.getElementById(modalId).classList.add('hidden');
    }
    
    // Cerrar modal al hacer clic fuera
    window.onclick = function(event) {
        const modalAprobar = document.getElementById('modal-aprobar');
        const modalRechazar = document.getElementById('modal-rechazar');
        
        if (event.target === modalAprobar) {
            modalAprobar.classList.add('hidden');
        }
        if (event.target === modalRechazar) {
            modalRechazar.classList.add('hidden');
        }
    }
    
    console.log('Vista Documentos por Revisar cargada');
</script>
